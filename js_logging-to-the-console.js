// Logging to the Console

// String
console.log('Hello World');

// Number
console.log(123);

// Boolean
console.log(true);
console.log(false);

// Variable
var greeting = 'Hello';
console.log(greeting);

// Array
console.log([1,2,3,4]);

// Object
console.log({a:1, b:2});

// Object in Table
console.table({a:1, b:2});

// Error
console.error('This is some error');

// Warning
console.warn('This is some warning');

// Timer
console.time('Hello');
    console.log('Hello World'); 
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
    console.log('Hello World');
console.timeEnd('Hello');

// Clear the Console
console.clear();